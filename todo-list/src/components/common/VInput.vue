<template>
  <div class="v-input">
    <input :type="type"
           :value="value"
           @input="change"
           class="v-input__text"
           :placeholder="placeholder" />
  </div>
</template>

<script>
export default {
  name: 'VInput',
  props: {
    type: {
      type: String,  // text, password, number
      default: 'text'
    },
    value: {
      type: String,
    },
    placeholder: {
      type: String
    }
  },
  methods: {
    change(event) {
      this.$emit('change', event.target.value);
    }
  }
};
</script>

